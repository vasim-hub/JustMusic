package com.justmusic.utils.appenums

/**
 * This Enum is used for handling different click events from
 * Menu Options Bottom Sheet Dialog.
 */
enum class MoreMenuClickEventsTypeEnum {
    /** Click event add favorite song*/
    CLICK_EVENT_FOR_ADD_FAVORITE_SONG,

    /** Click event remove favorite song */
    CLICK_EVENT_FOR_REMOVE_FAVORITE_SONG,

    /** Click event add favorite album*/
    CLICK_EVENT_FOR_ADD_FAVORITE_ALBUM,

    /** Click event remove favorite album*/
    CLICK_EVENT_FOR_REMOVE_FAVORITE_ALBUM,
}
